    public void Search(String account, String Password) {
        DB_connection.init();
        Connection c = DB_connection.getConnection();
        try {
            PreparedStatement ps = c.prepareStatement("SELECT accountnumber from accounts where accountnumber = '" + account + "';");
            ResultSet rs = ps.executeQuery();

            if (!rs.next()) {
                JOptionPane.showMessageDialog(null, "Incorrect Account number");

            } else {

                ps = c.prepareStatement("SELECT Password from accounts where Password = '" + Password + "';");
                rs = ps.executeQuery();
                if (rs.next()) {
                    PassV = rs.getString(1);
                }
            }

        } catch (SQLException ex) {

        }
    }

    void UpdateAccount(String Password) {
        DB_connection.init();
        Connection c = DB_connection.getConnection();
        try {
            PreparedStatement ps = c.prepareStatement("UPDATE accounts set Password = ? WHERE accountnumber = ?;");
            ps.setString(1, Password);
            ps.setString(2, AccountNumber);
            int rowsUpdated = ps.executeUpdate();
            
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Record updated successfully!");
            } else {
                JOptionPane.showMessageDialog(null, "No record found.");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Changepass.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Error: " + ex);
        }

    }

