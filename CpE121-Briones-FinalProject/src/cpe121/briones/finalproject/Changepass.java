    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String CurPass = CurrentPass.getText();
        String HashPass = enc.hash(CurPass);
        String newPass = newPasss.getText();
        String ConPass = ConfirmPass.getText();
        String HassnewPass = enc.hash(newPass);
        Search(AccountNumber, HashPass);
        if (CurPass.isEmpty() || newPass.isEmpty() || ConPass.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Fill information completely!");
        } else {
            if (HashPass.equals(PassV)) {
                if (newPass.equals(ConPass)) {
                    UpdateAccount(HassnewPass);
                    UserFront open = new UserFront(AccountNumber);
                    open.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "New Password did not match!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Current Password did not match!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

