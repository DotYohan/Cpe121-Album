    private void SilopinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SilopinActionPerformed
        String quant = Quantity.getText();
        int Validation = Integer.parseInt(quant);
        if (Validation >= 1) {
            ProductName = NewSilopin.getText();
            Id = "6";
            Price = SilopinValue.getText();
            quantity = Quantity.getText();

            Quantity.setText("1");
            pricing = Double.parseDouble(Price);
            quanty = Double.parseDouble(quant);
            total += pricing * quanty;
            Ftotal = String.valueOf(total);
            subtotal = pricing * quanty;
            oldmodel.addRow(new Object[]{Id, ProductName, "$" + Price, quantity, +subtotal});
            Total.setText(String.valueOf("$" + total));

        } else {
            JOptionPane.showMessageDialog(null, "Invalid Quantity!");
            Quantity.setText("1");
        }


    }//GEN-LAST:event_SilopinActionPerformed

