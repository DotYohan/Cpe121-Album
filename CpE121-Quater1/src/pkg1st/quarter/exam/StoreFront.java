    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        DefaultTableModel oldmodel = (DefaultTableModel) Cart.getModel();
        DefaultTableModel newmodel = new DefaultTableModel();
        for (int i = 0; i < oldmodel.getColumnCount(); i++) {
            newmodel.addColumn(oldmodel.getColumnName(i));
        }

        // Copy rows
        for (int i = 0; i < oldmodel.getRowCount(); i++) {
            Vector row = (Vector) oldmodel.getDataVector().get(i);
            newmodel.addRow(row.toArray()); // Convert to Object[]
        }

        // Open PaymentFront and pass the new model
        PaymentFront payment = new PaymentFront(newmodel);
        payment.setVisible(true);
        this.dispose();
        payment.CheckoutTotal(total);

    }//GEN-LAST:event_jButton1ActionPerformed

